url2 = "start=%s"
url3 = "&length=10&search%5Bvalue%5D=&search%5Bregex%5D=false&_=1532401103201"
get = requests.get
diff = set()
result = list()


def getalllistpage(p):
    u = url2 % p
    url = url1 + u + url3
    r = get(url)
    return r.json()["data"]


def makedata(raw):
    for v in raw:
        if v["id"] in diff:
            pass
        else:
            t = "enode://" + v["id"] + "@" + v["host"] + ":" + str(v["port"])
            result.append(t)
            diff.add(v["id"])


def savefile():
    with open("./nodes.log", "w") as f:
        for l in result:
            f.write(l + "\n")


def main():
    temp = list()
    for i in range(100):
        temp.extend(getalllistpage(str(i * 100)))
    makedata(temp)
    savefile()


if __name__ == '__main__':
    main()
